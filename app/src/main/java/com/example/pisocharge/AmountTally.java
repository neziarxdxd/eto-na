package com.example.pisocharge;

public class AmountTally {

    String unit;
    String amount;
    String date;

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getAmountTally() { return amount; }

    public void setAmountTally(String amount) {
        this.amount = amount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
